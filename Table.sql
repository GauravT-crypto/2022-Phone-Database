# Used create table method to create a table to compare the 
# top 10 2022 phones with 9 columns to compare specs
Create table Phone(
	Phone varchar(30) Primary Key,
    Brand varchar(20),
    Price varchar(20),
    Screen varchar(20),
    Pixels varchar(20),
    Resolution varchar(20),
    Storage_Size varchar(20),
    Ram varchar(20), 
    Battery varchar(20)
);

